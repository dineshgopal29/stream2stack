# ---------------------------------------------------------------------------
# Markdown → HTML converter
# ---------------------------------------------------------------------------


def markdown_to_html(markdown: str) -> str:
    """Convert a Markdown string to an HTML email with inline CSS.

    This is a self-contained, dependency-free converter that handles the
    Markdown subset generated by Claude (headings, bold, italic, code blocks,
    blockquotes, lists, horizontal rules, links). For production deployments
    consider replacing with `mistune` or `markdown2` for full spec compliance.

    Args:
        markdown: The Markdown content to convert.

    Returns:
        Full HTML document string with embedded CSS.
    """
    html = markdown

    # Escape HTML entities before processing.
    html = html.replace("&", "&amp;")
    html = html.replace("<", "&lt;")
    html = html.replace(">", "&gt;")

    # Fenced code blocks (``` ... ```)
    def replace_code_block(m: re.Match) -> str:
        code = m.group(2).strip()
        lang = m.group(1).strip() if m.group(1) else ""
        lang_attr = f' class="language-{lang}"' if lang else ""
        return f"<pre><code{lang_attr}>{code}</code></pre>"

    html = re.sub(r"```(\w*)\n(.*?)```", replace_code_block, html, flags=re.DOTALL)

    # Inline code (`code`)
    html = re.sub(r"`([^`]+)`", r"<code>\1</code>", html)

    # YAML frontmatter block — strip it.
    html = re.sub(r"^---\n.*?\n---\n\n?", "", html, flags=re.DOTALL)

    # Headings
    for level in range(6, 0, -1):
        pattern = r"^" + "#" * level + r"\s+(.+)$"
        html = re.sub(pattern, rf"<h{level}>\1</h{level}>", html, flags=re.MULTILINE)

    # Bold (**text** or __text__)
    html = re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", html)
    html = re.sub(r"__(.+?)__", r"<strong>\1</strong>", html)

    # Italic (*text* or _text_)
    html = re.sub(r"\*([^*\n]+)\*", r"<em>\1</em>", html)
    html = re.sub(r"_([^_\n]+)_", r"<em>\1</em>", html)

    # Blockquotes
    html = re.sub(r"^&gt;\s*(.+)$", r"<blockquote>\1</blockquote>", html, flags=re.MULTILINE)

    # Horizontal rules
    html = re.sub(r"^---+$", r"<hr>", html, flags=re.MULTILINE)
    html = re.sub(r"^\*\*\*+$", r"<hr>", html, flags=re.MULTILINE)

    # Links [text](url)
    html = re.sub(r"\[([^\]]+)\]\(([^)]+)\)", r'<a href="\2">\1</a>', html)

    # Unordered lists (lines starting with - or *)
    def replace_ul(m: re.Match) -> str:
        items = re.findall(r"^[*\-]\s+(.+)$", m.group(0), re.MULTILINE)
        lis = "".join(f"<li>{item.strip()}</li>" for item in items)
        return f"<ul>{lis}</ul>"

    html = re.sub(r"(?:^[*\-]\s+.+\n?)+", replace_ul, html, flags=re.MULTILINE)

    # Ordered lists
    def replace_ol(m: re.Match) -> str:
        items = re.findall(r"^\d+\.\s+(.+)$", m.group(0), re.MULTILINE)
        lis = "".join(f"<li>{item.strip()}</li>" for item in items)
        return f"<ol>{lis}</ol>"

    html = re.sub(r"(?:^\d+\.\s+.+\n?)+", replace_ol, html, flags=re.MULTILINE)

    # Paragraphs — wrap blocks of text not already in an HTML tag.
    def wrap_paragraphs(text: str) -> str:
        lines = text.split("\n")
        output: list[str] = []
        para_lines: list[str] = []

        def flush_para():
            if para_lines:
                para = " ".join(para_lines).strip()
                if para:
                    output.append(f"<p>{para}</p>")
                para_lines.clear()

        for line in lines:
            stripped = line.strip()
            if not stripped:
                flush_para()
            elif re.match(r"^<(h[1-6]|ul|ol|li|pre|blockquote|hr|p)", stripped):
                flush_para()
                output.append(stripped)
            else:
                para_lines.append(stripped)

        flush_para()
        return "\n".join(output)

    html = wrap_paragraphs(html)

    return (
        f'<!DOCTYPE html>\n<html lang="en">\n<head>\n'
        f'<meta charset="UTF-8">\n'
        f'<meta name="viewport" content="width=device-width, initial-scale=1.0">\n'
        f"<style>{_EMAIL_CSS}</style>\n"
        f"</head>\n<body>\n"
        f'<div class="wrapper">\n{html}\n</div>\n'
        f'<div class="wrapper footer">'
        f"You're receiving this because you subscribed to Stream2Stack. "
        f"</div>\n"
        f"</body>\n</html>"
    )


